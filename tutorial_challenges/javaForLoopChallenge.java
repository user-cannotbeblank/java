package tutorial_challenges;

/*
Inside main(), complete the following steps:
- Declare int i (either before the loop or inside the for)
- Use a for loop, and:
> Set i = 1
> Run the loop while i <= 5
> Increase i using i++
> Inside the loop, print i
*/

public class javaForLoopChallenge {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
