package methods_challenges;

/*
Inside Main, complete the following steps:

- Create a method named welcomeMessage
- The method should print Welcome to Java!
- Call welcomeMessage() inside main()

Note: The method must be static.
*/

public class javaMethodChallenge {
    public static void main(String[] args) {
        welcomeMessage(); // Calls welcomeMessage Method.
    }

    // welcomeMessage Method
    static void welcomeMessage() {
        System.out.println("Welcome to Java!"); // Prints "Welcome to Java!"
    }
}
